# Crie um algoritmo que leia um número e mostre o seu dobro, tripro e a raiz quadrada

numero = int(input('Digite um número: '))
dobro = numero * 2
triplo = numero * 3 
raiz = numero ** 0.5
print('Seu número é {}, o dobro é {}, e o triplo {}, a raiz quadrada é {},'.format(numero, dobro, triplo, raiz))